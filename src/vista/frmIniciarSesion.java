    /**
     * Este metodo detecta la huella dactilar y abre la ventana de habitante o trabajador segun la huella detectada
     */
    private void detectaHuella() {
        
        // Esta linea de abajo se debe cambiar para adaptar al detector de huellas digitales
        int huellaDactilar = 0;
        if(huellaDactilar == 0){
            frmTrabajador frame = new frmTrabajador();
            frame.setVisible(true);
            this.dispose();
            this.setVisible(false);
        } else if (huellaDactilar == 1){
            frmHabitante frame = new frmHabitante();
            frame.setVisible(true);
            this.dispose();
            this.setVisible(false);
        }
    }
